import turtle
t=turtle.Pen()
t.color("blue")
t.begin_fill()
t.setheading(90)
t.forward(180)
t.setheading(180)
t.circle(180,90)
t.end_fill()
t.setheading(0)
t.forward(180)
t.color("green")
t.begin_fill()
t.forward(180)
t.setheading(90)
t.circle(180,90)
t.end_fill()
t.up()
t.setheading(270)
t.forward(180)
t.down()
t.color("yellow")
t.begin_fill()
t.setheading(270)
t.forward(180)
t.setheading(0)
t.circle(180,90)
t.end_fill()
t.up()
t.setheading(180)
t.forward(180)
t.down()
t.color("red")
t.begin_fill()
t.forward(180)
t.setheading(270)
t.circle(180,90)
t.end_fill()
t.up()
t.setheading(0)
t.forward(180)
t.setheading(90)
t.forward(180)
t.down()
t.pencolor('white')
t.width(5)
t.circle(90)
t.up()
t.setheading(180)
t.forward(180)
t.down()
t.setheading(90)
t.circle(90)
